package setexpansion.entity;

public class TermInfo {

	String term;
	String termCount;
	public String getTerm() {
		return term;
	}
	public void setTerm(String term) {
		this.term = term;
	}
	public String getTermCount() {
		return termCount;
	}
	public void setTermCount(String termCount) {
		this.termCount = termCount;
	}

	
}
